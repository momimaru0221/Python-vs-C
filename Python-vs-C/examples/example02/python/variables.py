# variables.py
# このPythonスクリプトは、異なる型の変数を宣言し、それらを出力することで、動的型付けの概念を示します。

integerVar = 42         # 整数
floatVar = 3.14         # 浮動小数点
charVar = 'A'           # 文字
stringVar = "Hello, Python!" # 文字列

# 各変数の値と型を出力
print(f"Integer: {integerVar}")
print(f"Float: {floatVar}")
print(f"Char: {charVar}")
print(f"String: {stringVar}")